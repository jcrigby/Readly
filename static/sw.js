const CACHE_NAME = 'readly-v1';
const STATIC_ASSETS = ['/', '/manifest.json', '/favicon.png'];
const BUNDLE_URL = '/feed-bundle.json';

self.addEventListener('install', (event) => {
	event.waitUntil(caches.open(CACHE_NAME).then((cache) => cache.addAll(STATIC_ASSETS)));
	self.skipWaiting();
});

self.addEventListener('activate', (event) => {
	event.waitUntil(
		caches
			.keys()
			.then((keys) =>
				Promise.all(keys.filter((key) => key !== CACHE_NAME).map((key) => caches.delete(key)))
			)
	);
	self.clients.claim();
});

self.addEventListener('fetch', (event) => {
	const url = new URL(event.request.url);

	// Network-first for the feed bundle (get fresh data, fall back to cache)
	if (url.pathname === BUNDLE_URL) {
		event.respondWith(
			fetch(event.request)
				.then((response) => {
					const clone = response.clone();
					caches.open(CACHE_NAME).then((cache) => cache.put(event.request, clone));
					return response;
				})
				.catch(() => caches.match(event.request))
		);
		return;
	}

	// Cache-first for static assets (app shell)
	if (event.request.method === 'GET' && url.origin === self.location.origin) {
		event.respondWith(
			caches.match(event.request).then((cached) => {
				const fetchPromise = fetch(event.request).then((response) => {
					const clone = response.clone();
					caches.open(CACHE_NAME).then((cache) => cache.put(event.request, clone));
					return response;
				});
				return cached || fetchPromise;
			})
		);
		return;
	}
});
